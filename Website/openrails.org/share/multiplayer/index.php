<h2>Multi-Player Operation</h2>
<p>
Since mid-2012 the Open Rails team has been developing a facility for several players to work together, each operating their own locomotive.
There are lots of possibilities here, with drivers operating connecting services or two locomotive working together (double-heading) or, with steam power, as driver and fireman. 
</p>
<h3>Dispatcher Operation</h3>
<p>
Another possibility is for one player to take the role of dispatcher or train controller in charge of operations, using the switches,  signals and radio messages to coordinate trains in response to events and in more detail than a timetable. 
</p>
<h3>Dispatcher Window</h3>
<p>
Driver and fireman is still an item on our wish list but the other capabilities are all in place. An independent window is provided for the
dispatcher (or any player) to monitor the location of trains and operate switches and signals.
</p><p>
<img src="dispatcher_window.jpg" title="Dispatcher Window" alt="Dispatcher Window" width=600 height=517 />
</p>
<h3>Communicating</h3>
<p>
Players can communicate using the built-in text message but many groups prefer to use a free system such as Skype which can provide voice as well. Their PCs communicate in a flexible way as follows. Any player may launch his Open Rails as a server, publish its IP address and other players launch their Open Rails as clients connecting to that address. See <a href="../../learn/manual-and-tutorials/">the manual</a> for more details.
</p><p>
There is also <a href="http://www.tsimserver.com/forums">a public forum</a> where you can find and announce multi-player sessions and discuss multi-player issues.
</p>
